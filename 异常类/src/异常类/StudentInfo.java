package 异常类;
//创造一个类，用于输入学生的信息（返回的类型）
class StudentInfo {
    	int number;
    	String name;
    	float score;
    	public StudentInfo() {
    	}
    	public StudentInfo(int number, String name,float score) {
    		this.number = number;
    		this.name = name;
    		this.score = score;
    	}
}
